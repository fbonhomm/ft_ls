#include "ft_ls.h"

int		len_dir(char **av, t_stc *stc)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (av[i])
	{
		if ((av[i][0] != '-') || (av[i][0] == '-' && stc->not_arg == 1) ||
				(av[i][0] == '-' && !av[i][1]))
			j++;
		i++;
	}
	return (j);
}
